package capitulo1;

public class Variaveis {

	public static void main(String[] args) {
		byte a = 10;
		long ano = 2021L;
		a = (byte) ano;
		int b = 0;
		int idade = 22;
		char sexo = 'f';
		String curso = "Linguagem java", escola = "impacta";
		double VAL_PI = 3.1415;
		float faixa = 4.21f;
		boolean resposta = true;

		b = 20;
		System.out.println("O valor de a é: " + a);
		System.out.println("O valor de b é: " + b);
		System.out.println("O valor de idade é: " + idade);
		System.out.println("O valor de sexo é: " + sexo);
		System.out.println("O valor de curso é" + curso);
		System.out.println("O valor de escola é" + escola);
		System.out.println("O valor de VAL_PI é" + VAL_PI);
		System.out.println("O valor de faixa é" + faixa);
		System.out.println("O valor de faixa é" + resposta);


	}

}
